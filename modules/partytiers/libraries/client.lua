function MODULE:DrawCharInfo(client, character, info)
    local tierNames = {
        "Tier I Party Member",
        "Tier II Party Member",
        "Tier III Party Member",
        "Tier IV Party Member",
        "Tier V Party Member"
    }

    local tier = character:getData("party_tier", 0)

    if tier >= 1 and tier <= 5 then
        info[#info + 1] = {tierNames[tier], Color(255, 209, 20)}
    end
end

